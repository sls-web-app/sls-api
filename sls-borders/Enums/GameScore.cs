namespace sls_borders.Enums;

/// <summary>
/// Defines the possible outcomes of a game.
/// </summary>
public enum GameScore
{
    /// <summary>
    /// Indicates that the white player has won the game.
    /// </summary>
    WhiteWin,

    /// <summary>
    /// Indicates that the game ended in a draw.
    /// </summary>
    Draw,

    /// <summary>
    /// Indicates that the black player has won the game.
    /// </summary>
    BlackWin,
}
